# ━━ Table Helper ━━
def make_table(headers, rows, col_widths=None, caption=None):
    """Build a professional table with palette colors and Paragraph-wrapped cells."""
    data = []
    header_row = [Paragraph('<b>%s</b>' % h, style_table_header) for h in headers]
    data.append(header_row)
    for row in rows:
        data.append([Paragraph(str(c), style_table_cell) if not isinstance(c, Paragraph) else c for c in row])

    if col_widths is None:
        n = len(headers)
        col_widths = [AVAILABLE_W / n] * n
    else:
        total = sum(col_widths)
        if total < AVAILABLE_W * 0.85:
            scale = (AVAILABLE_W * 0.95) / total
            col_widths = [w * scale for w in col_widths]

    tbl = Table(data, colWidths=col_widths, hAlign='CENTER')
    style_cmds = [
        ('BACKGROUND', (0, 0), (-1, 0), TABLE_HEADER_COLOR),
        ('TEXTCOLOR', (0, 0), (-1, 0), TABLE_HEADER_TEXT),
        ('GRID', (0, 0), (-1, -1), 0.5, TEXT_MUTED),
        ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
        ('LEFTPADDING', (0, 0), (-1, -1), 8),
        ('RIGHTPADDING', (0, 0), (-1, -1), 8),
        ('TOPPADDING', (0, 0), (-1, -1), 5),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 5),
    ]
    for i in range(1, len(data)):
        bg = TABLE_ROW_EVEN if i % 2 == 1 else TABLE_ROW_ODD
        style_cmds.append(('BACKGROUND', (0, i), (-1, i), bg))
    tbl.setStyle(TableStyle(style_cmds))

    elements = [Spacer(1, 18), tbl]
    if caption:
        elements.append(Spacer(1, 6))
        elements.append(Paragraph(caption, style_caption))
    elements.append(Spacer(1, 12))
    return elements

