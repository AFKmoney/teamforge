def render_cover():
    """Render the cover HTML to PDF using Playwright directly."""
    cover_js = f"""
const {{ chromium }} = require('playwright');
(async () => {{
  const browser = await chromium.launch({{ headless: true }});
  const page = await browser.newPage({{ viewport: {{ width: 794, height: 1200 }} }});
  await page.goto('file://{COVER_HTML}', {{ waitUntil: 'networkidle' }});
  const poster = await page.evaluate(() => {{
    const el = document.querySelector('.cover') || document.body;
    return {{ h: el.scrollHeight, w: el.scrollWidth }};
  }});
  console.log('Content size:', poster.w, 'x', poster.h);
  await page.pdf({{
    path: '{COVER_PDF}',
    width: '794px',
    height: poster.h + 'px',
    printBackground: true,
    margin: {{ top: '0', right: '0', bottom: '0', left: '0' }},
  }});
  await browser.close();
  console.log('Cover PDF generated!');
}})();
"""
    result = subprocess.run(
        ['node', '-e', cover_js],
        capture_output=True, text=True, timeout=60,
        cwd=str(OUTPUT_DIR),
    )
    print(result.stdout)
    if result.returncode != 0:
        print(f"Cover render stderr: {result.stderr}", file=sys.stderr)
        raise RuntimeError(f"Cover render failed: {result.stderr}")
    print(f"Cover PDF rendered: {COVER_PDF}")


# ━━ MERGE COVER + BODY ━━
def merge_pdfs():
    """Merge cover PDF as page 0 with body PDF using pypdf."""
    from pypdf import PdfReader, PdfWriter, Transformation

    A4_W, A4_H = 595.28, 841.89

    def normalize_page(page):
        box = page.mediabox
        w, h = float(box.width), float(box.height)
        if abs(w - A4_W) > 2 or abs(h - A4_H) > 2:
            sx, sy = A4_W / w, A4_H / h
            page.add_transformation(Transformation().scale(sx=sx, sy=sy))
            page.mediabox.lower_left = (0, 0)
            page.mediabox.upper_right = (A4_W, A4_H)
        return page

    writer = PdfWriter()
    # Cover as page 1
    cover_page = PdfReader(str(COVER_PDF)).pages[0]
    writer.add_page(normalize_page_to_a4(cover_page))
    # Body pages follow
    for page in PdfReader(str(BODY_PDF)).pages:
        writer.add_page(normalize_page_to_a4(page))

    writer.add_metadata({
        '/Title': 'TeamForge IDE Instruction Manual',
        '/Author': 'Z.ai',
        '/Creator': 'Z.ai',
        '/Subject': 'Instruction manual for TeamForge IDE v1.0.0',
    })
    with open(str(FINAL_PDF), 'wb') as f:
        writer.write(f)
    print(f"Final PDF merged: {FINAL_PDF}")


def normalize_page_to_a4(page):
    """Scale a page to A4 if its dimensions don't match."""
    from pypdf import Transformation
    A4_W, A4_H = 595.28, 841.89
    box = page.mediabox
    w, h = float(box.width), float(box.height)
    if abs(w - A4_W) > 2 or abs(h - A4_H) > 2:
        sx, sy = A4_W / w, A4_H / h
        page.add_transformation(Transformation().scale(sx=sx, sy=sy))
        page.mediabox.lower_left = (0, 0)
        page.mediabox.upper_right = (A4_W, A4_H)
    return page


